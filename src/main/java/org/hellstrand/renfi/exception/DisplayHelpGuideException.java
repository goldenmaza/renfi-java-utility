package org.hellstrand.renfi.exception;

public class DisplayHelpGuideException extends ExpectedRuntimeException {
    public DisplayHelpGuideException() {
        super();
    }

    public DisplayHelpGuideException(String message) {
        super(message);
    }

    public DisplayHelpGuideException(String message, Throwable cause) {
        super(message, cause);
    }

    public DisplayHelpGuideException(String message, Throwable cause, boolean enableSuppression, boolean writeableStackTrace) {
        super(message, cause, enableSuppression, writeableStackTrace);
    }
}
